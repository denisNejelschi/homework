package homework26.task02;

public class Truck {

  String model;
  String fuelType;
  double price;
  double range;
  double consumption;
  double maxWeight;
  double maxPayload;

  public Truck(String model, String fuelType, double price, double range, double consumption,
      double maxWeight, double maxPayload) {

    this.model = model;
    this.fuelType = fuelType;
    this.price = price;
    this.range = range;
    this.consumption = consumption;
    this.maxWeight = maxWeight;
    this.maxPayload = maxPayload;
  }



}
